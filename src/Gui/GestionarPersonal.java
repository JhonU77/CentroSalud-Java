    public void cargarDatosTabla() {
        String[] columnas = {"DNI", "Nombres", "Apellido Paterno", "Apellido Materno", "Fecha Nac.", "Género", "Condición", "Profesión"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);

        String sql = "SELECT p.DNI_Personal, p.Nombres_Personal, p.ApellidoPaterno_Personal, p.ApellidoMaterno_Personal, p.FechaNacimiento_Personal, p.Genero_Personal, c.Desc_Condicion, pr.Desc_Profesion " +
                     "FROM Personal p " +
                     "JOIN Condicion c ON p.ID_Condicion = c.ID_Condicion " +
                     "JOIN Profesion pr ON p.ID_Profesion = pr.ID_Profesion";

        try (Connection con = Conexion.getConexion();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                Object[] fila = new Object[8];
                fila[0] = rs.getString("DNI_Personal");
                fila[1] = rs.getString("Nombres_Personal");
                fila[2] = rs.getString("ApellidoPaterno_Personal");
                fila[3] = rs.getString("ApellidoMaterno_Personal");
                fila[4] = rs.getDate("FechaNacimiento_Personal");
                fila[5] = rs.getString("Genero_Personal");
                fila[6] = rs.getString("Desc_Condicion");
                fila[7] = rs.getString("Desc_Profesion");
                modelo.addRow(fila);
            }

            jTablePersonal.setModel(modelo);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos en la tabla: " + e.getMessage());
        }
    }



