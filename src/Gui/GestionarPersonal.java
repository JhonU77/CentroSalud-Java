    public void cargarProfesiones(JComboBox<String> jCombProfesion) {
        String sql = "SELECT Desc_Profesion FROM Profesion";

        Connection con = null;
        Statement st = null;
        ResultSet rs = null;

        try {
            con = Conexion.getConexion();
            st = con.createStatement();
            rs = st.executeQuery(sql);

            jCombProfesion.removeAllItems();
            while (rs.next()) {
                jCombProfesion.addItem(rs.getString("Desc_Profesion"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar profesiones: " + e.getMessage());
        }
    }
    
    public void cargarTablaPersonal(JTable jTablePersonal) {
        String sql = "SELECT p.DNI_Personal, p.Nombres_Personal, p.ApellidoPaterno_Personal, " +
                     "p.ApellidoMaterno_Personal, p.FechaNacimiento_Personal, p.Genero_Personal, " +
                     "c.Desc_Condicion, pr.Desc_Profesion " +
                     "FROM Personal p " +
                     "INNER JOIN Condicion c ON p.ID_Condicion = c.ID_Condicion " +
                     "INNER JOIN Profesion pr ON p.ID_Profesion = pr.ID_Profesion " +
                     "ORDER BY p.Nombres_Personal";

        try (Connection con = Conexion.getConexion();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(sql)) {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("DNI");
            model.addColumn("Nombres");
            model.addColumn("Apellido Paterno");
            model.addColumn("Apellido Materno");
            model.addColumn("Fecha Nacimiento");
            model.addColumn("Género");
            model.addColumn("Condición");
            model.addColumn("Profesión");

            while (rs.next()) {
                Object[] fila = new Object[8];
                fila[0] = rs.getString("DNI_Personal");
                fila[1] = rs.getString("Nombres_Personal");
                fila[2] = rs.getString("ApellidoPaterno_Personal");
                fila[3] = rs.getString("ApellidoMaterno_Personal");
                fila[4] = rs.getDate("FechaNacimiento_Personal");
                fila[5] = rs.getString("Genero_Personal");
                fila[6] = rs.getString("Desc_Condicion");
                fila[7] = rs.getString("Desc_Profesion");

                model.addRow(fila);
            }

            jTablePersonal.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar tabla personal: " + e.getMessage());
        }
    }

    
